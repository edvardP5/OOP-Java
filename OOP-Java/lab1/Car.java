class Car {
int horsepower;
double engineVolume;
String brand;
String model;
int year;
String color;
boolean isElectric;
double weight;

Car(int horsepower, double engineVolume, String brand, String model,
    int year, String color, boolean isElectric, double weight) {
    this.horsepower = horsepower;
    this.engineVolume = engineVolume;
    this.brand = brand;
    this.model = model;
    this.year = year;
    this.color = color;
    this.isElectric = isElectric;
    this.weight = weight;
}
}
